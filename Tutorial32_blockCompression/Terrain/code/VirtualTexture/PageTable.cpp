void doit( int value, int offsetY, unsigned char * pix ) {
	// Add debug coordinates to the page
	int tmp = value;
	int offset = PAGE_WIDTH - GLYPH_WIDTH;
	do {
		int * glyph = NULL;
		switch ( tmp % 10 ) {
			case 0: glyph = g_0; break;
			case 1: glyph = g_1; break;
			case 2: glyph = g_2; break;
			case 3: glyph = g_3; break;
			case 4: glyph = g_4; break;
			case 5: glyph = g_5; break;
			case 6: glyph = g_6; break;
			case 7: glyph = g_7; break;
			case 8: glyph = g_8; break;
			case 9: glyph = g_9; break;
		}

		const int halfGlyph = GLYPH_WIDTH / 2;
		for ( int y = 0; y < halfGlyph; y++ ) {
			for ( int x = 0; x < halfGlyph; x++ ) {
				int factor = glyph[ x * 2 + GLYPH_WIDTH * y * 2 ];
				int idx = offset + x + PAGE_WIDTH * ( y + offsetY );
				if ( idx > 127 * 127 ) {
					continue;
				}
				pix[ idx * 4 + 0 ] *= factor;
				pix[ idx * 4 + 1 ] *= factor;
				pix[ idx * 4 + 2 ] *= factor;
				//pix[ idx * 4 + 3 ] *= factor;
			}
		}

		tmp /= 10;
		offset -= halfGlyph;
	} while ( tmp > 0 && offset > 16 );
}

